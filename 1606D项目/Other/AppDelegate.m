                                    authType:SSDKAuthTypeBoth];
                 break;
                 //如果人人视频报错直接删掉就好了  从  case 删除到 break
             case SSDKPlatformTypeRenren:
                 [appInfo        SSDKSetupRenRenByAppId:@"226427"
